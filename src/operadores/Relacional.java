package operadores;

public class Relacional {

	public static void main(String[] args) {
		int numero1 = 19;
		int numero2 = 20;
		
		System.out.println(numero1 > numero2);
		System.out.println(numero1 >= numero2);
		System.out.println(numero1 < numero2);
		System.out.println(numero1 <= numero2);
	}
	
}
